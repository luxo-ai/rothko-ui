import { useEffect, useLayoutEffect, useState } from 'react';
import { createPortal } from 'react-dom';

export const PORTAL_ROOT_ID = 'portal-root';

// compantibility with SSR
const useIsomorphicLayoutEffect = typeof window !== 'undefined' ? useLayoutEffect : useEffect;

type PortalProps = {
  children: React.ReactNode;
  wrapperId: string;
};

const DomPortal = ({ children, wrapperId }: PortalProps) => {
  const [wrapperEl, setWrapperEl] = useState<HTMLElement | null>(null);

  useIsomorphicLayoutEffect(() => {
    let element = document.getElementById(wrapperId);
    let elementAutoGenerated = false;
    if (!element) {
      elementAutoGenerated = true;
      element = createWrapperAndAppend(wrapperId, PORTAL_ROOT_ID);
    }
    setWrapperEl(element);
    return () => {
      // delete auto-created element
      if (elementAutoGenerated) {
        element?.parentNode?.removeChild(element);
      }
    };
  }, [wrapperId]);

  return wrapperEl ? createPortal(children, wrapperEl) : null;
};

const createWrapperAndAppend = (wrapperId: string, portalId: string) => {
  const wrapperElement = document.createElement('div');
  wrapperElement.setAttribute('id', wrapperId);
  const portalEl = document.getElementById(portalId);
  if (!portalEl) {
    document.body.appendChild(wrapperElement);
  }
  // default to body if not found
  else {
    portalEl.appendChild(wrapperElement);
  }
  return wrapperElement;
};

export default DomPortal;
